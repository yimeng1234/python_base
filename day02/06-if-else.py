"""
if - else

语法：

if 条件判断：
    条件满足，执行代码1
    条件满足，执行代码2
    。。。
else:
    条件不满足，执行代码1
    条件不满足，执行代码2

注意事项
1.  if 和 else 他是一个整体 我们不能单独使用 else
2. else 要和 if 对齐
3. else 后边要添加 冒号！！！

"""
